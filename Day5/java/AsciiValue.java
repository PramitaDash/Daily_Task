package com.java.java;

public class AsciiValue {
	public static void main(String[] args) {
		char ch1 ='a';
		char ch2 = 'b';
		int ch1value= ch1;
		int ch2value = ch2;
		
		System.out.println("The ASCII value of"+ ch1+ "is:" + ch1value);
		System.out.println("The ASCII value of "+ch2+ "is:"+ch2value);
	}

}
